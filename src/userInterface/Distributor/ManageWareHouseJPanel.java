    private void populateWarehouseTable() {
        DefaultTableModel model = (DefaultTableModel) tblWarehouses.getModel();
        model.setRowCount(0);
        
        if(!distributorEnterprise.getWarehouseList().isEmpty())
        {
            for(Warehouse w : distributorEnterprise.getWarehouseList())
            {
                Object [] row = new Object[2];
                row[0] = w;
                row[1] = w.getWarehouseRegion();
                model.addRow(row);
            
            }
        
        }
    }

