    private void btnViewInventoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewInventoryActionPerformed
        // TODO add your handling code here:
        int rowSelected = tblWarehouses.getSelectedRow();
        
        if(rowSelected<0)
        {
            JOptionPane.showMessageDialog(null,"No row selected!");
            return;
        
        }
        Warehouse w = (Warehouse) tblWarehouses.getValueAt(rowSelected, 0);
        
        ViewWarehouseInventoryJPanel vwijp = new ViewWarehouseInventoryJPanel(userProcessContainer,system,w);
        userProcessContainer.add("ViewWarehouseInventoryJPanel", vwijp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        
    }//GEN-LAST:event_btnViewInventoryActionPerformed

    private void btnAddWareHouseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddWareHouseActionPerformed
        // TODO add your handling code here:
        
        String region = (String) comboRegion.getSelectedItem();
        boolean flag = false;
        
        if(!distributorEnterprise.getWarehouseList().isEmpty())
        {
            for(Warehouse w : distributorEnterprise.getWarehouseList())
            {
                if(w.getWarehouseRegion().equals(region))
                {
                    flag = true;
                
                }
            
            }
        
        }
        
        
        if(flag == false)
        {
            Warehouse w = new Warehouse();
            w.setWarehouseRegion(region);
            distributorEnterprise.getWarehouseList().add(w);
            
        }
        else
        {
            JOptionPane.showMessageDialog(null, "There is a warehouse in this region");
            
        }
        
        populateWarehouseTable();
    }//GEN-LAST:event_btnAddWareHouseActionPerformed

