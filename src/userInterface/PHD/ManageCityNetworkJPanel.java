    private void btnAddCityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCityActionPerformed
        // TODO add your handling code here:
        String name = txtCity.getText().trim();
        boolean flag = false;
        
        if(!name.isEmpty()){
            if(!cityNetworkDirectory.getCityNetworkDirectory().isEmpty()){
                for(CityNetwork cn : cityNetworkDirectory.getCityNetworkDirectory()){
                    if(cn.getCityName().equals(name)){
                        JOptionPane.showMessageDialog(null, "City already exists!");
                        flag = true;
                    }   
                }
            }else{
                CityNetwork city = cityNetworkDirectory.addCityNetwork(name);
                city.setInState(stateNetwork.getState().getName());
                flag = true;
            }
        }else{
            JOptionPane.showMessageDialog(null, "Enter a name for the city!");
            flag = true;
        }
        
        if(flag == false){
            CityNetwork city = cityNetworkDirectory.addCityNetwork(name);
            city.setInState(stateNetwork.getState().getName());
        }
        
        populateTbl();
        txtCity.setText("");
    }//GEN-LAST:event_btnAddCityActionPerformed

