    private void populateTbl(){
        DefaultTableModel dtm = (DefaultTableModel) tblCity.getModel();
        dtm.setRowCount(0);
        
        for(CityNetwork cn : cityNetworkDirectory.getCityNetworkDirectory()){
            Object[] row = new Object[1];
            row[0] = cn;
            dtm.addRow(row);
        }
    }

