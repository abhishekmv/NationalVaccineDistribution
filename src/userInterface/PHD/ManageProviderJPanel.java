    public void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) providerJTable.getModel();
        dtm.setRowCount(0);
        
        for(Enterprise e : enterpriseDirectory.getEnterpriseList()){
            if(e.getEnterpriseType().equals(Enterprise.EnterpriseType.Provider)){
                Object[] row = new Object[2];
                row[0] = e.getOrganizationID();
                row[1] = e;
                dtm.addRow(row);
            }
        }
        
    }

