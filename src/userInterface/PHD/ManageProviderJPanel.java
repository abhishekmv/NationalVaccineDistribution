        // TODO add your handling code here:
        addProviderJLayeredPane.setVisible(true);
          boolean flag = false;
        for(Enterprise e : enterpriseDirectory.getEnterpriseList()){
            if(e instanceof ProviderEnterprise){
                flag = true;
                JOptionPane.showMessageDialog(null, "A State Can Have Only One Provider!");
                addProviderJLayeredPane.setVisible(false);
                
            }
        }
        
