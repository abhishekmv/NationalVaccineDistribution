    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText().trim();
        EnterpriseType type = (EnterpriseType) enterpriseTypeJCombox.getSelectedItem();
        
        if(!name.isEmpty()){
            boolean flag = enterpriseDirectory.checkExisting(name, type);
            
            if(flag == false){
                Enterprise enterprise = enterpriseDirectory.createAndAddEnterprise(name, type);
                if(type.getValue().equals(Enterprise.EnterpriseType.Distributor.getValue()))
                {
                    enterprise.getOrganizationDirectory().createOrganization(Organization.Type.Shipping);
                }
                //enterprise.setEnterpriseType(type);
                txtName.setText("");
                
            }else{
                JOptionPane.showMessageDialog(null, "The enterprise already exists!");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter A Name For The Enterprise!");
        }
        populateTable();
        txtName.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed

