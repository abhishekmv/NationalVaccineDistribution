      public void populateCombo(){
        enterpriseTypeJCombox.removeAllItems();
        
        enterpriseTypeJCombox.addItem(Enterprise.EnterpriseType.Distributor);
        enterpriseTypeJCombox.addItem(Enterprise.EnterpriseType.Manufacturer);

    }
    
    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) enterpriseJTable.getModel();
        dtm.setRowCount(0);
        
        for(Enterprise e : enterpriseDirectory.getEnterpriseList()){
            Object[] row = new Object[3];
            row[0] = e.getOrganizationID();
            row[1] = e.getName();
            row[2] = e.getEnterpriseType();
            dtm.addRow(row);
        }
    }
    
    
