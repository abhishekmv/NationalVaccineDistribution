    private void addDiseaseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDiseaseJButtonActionPerformed
        // TODO add your handling code here:
         addEditJLayeredPane.setVisible(false);
         
           String name = txtName.getText().trim();
        String symp = txtSymp.getText().trim();
        
        
        if(!name.isEmpty()){
            if(!symp.isEmpty()){
                
                    boolean flag = diseaseDirectory.checkExisting(name);
                    
                    if(flag == false){
                        Disease disease = diseaseDirectory.createAndAddDisease();
                        disease.getSymptom().setSymptomsDescriptions(symp);
                        disease.setDiseaseName(name);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Disease Exists In The System!");
                    }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please Enter Symptom ");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Enter A Name For The Disease!");
        }
        populateTable();
        txtName.setText("");
        txtSymp.setText("");
    }//GEN-LAST:event_addDiseaseJButtonActionPerformed

    private void viewJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewJButtonActionPerformed
        // TODO add your handling code here:
         
         
         int rowSelected = tblDisease.getSelectedRow();
        
        if(rowSelected<0)
        {
             JOptionPane.showMessageDialog(null, "No row is selected");
            return;
        
        }
        
        addEditJLayeredPane.setVisible(true);
         editJLabel.setText("Edit Disease");
         addDiseaseJButton.setEnabled(false);
         updateJButton.setEnabled(true);
         txtName.setEditable(false);
         txtSymp.setEditable(false);
        saveJButton.setEnabled(false);
        Disease disease =(Disease) tblDisease.getValueAt(rowSelected, 0);
        
        txtName.setText(disease.getDiseaseName());
        txtSymp.setText(disease.getSymptom().getSymptomsDescriptions());
        
         
       
    }//GEN-LAST:event_viewJButtonActionPerformed

