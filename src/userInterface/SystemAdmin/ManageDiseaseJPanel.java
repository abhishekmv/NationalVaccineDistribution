    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) tblDisease.getModel();
        dtm.setRowCount(0);
        
        for(Disease d : diseaseDirectory.getDiseaseList()){
            Object[] row = new Object[2];
            
            row[0] = d;
            row[1] = d.getSymptom().getSymptomsDescriptions();
            dtm.addRow(row);
        }
    }
    
    
