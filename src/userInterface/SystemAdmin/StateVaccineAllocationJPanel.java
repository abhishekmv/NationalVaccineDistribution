    private void viewStateAllocationJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewStateAllocationJButtonActionPerformed
        // TODO add your handling code here:
        int rowSelected = currInventoryJTable.getSelectedRow();
        
        if(rowSelected<0)
        {
            JOptionPane.showMessageDialog(null, "No row selected");
            return;
        }
        
        Vaccine vac = (Vaccine) currInventoryJTable.getValueAt(rowSelected, 1);
        vaccineNameJTextField.setText(vac.getVaccineName());
        vaccineTypeJTextField.setText(vac.getVaccineType().getVaccineTypeName());
        
        DefaultTableModel model = (DefaultTableModel) stateDistributionJTable.getModel();
        model.setRowCount(0);
        
        
        for(StateNetwork sn : system.getStateNetworkDirectory().getStateNetwork())
        {
            Object[] row = new Object[3];
            row[0] = sn.getState().getName();
            row[1] = system.totalPercentage(system.totalPopulation(), sn.getState().getName());
            
            int percentage  = (int)(system.totalPercentage(system.totalPopulation(), sn.getState().getName()));
            
            int vacAllocationNo = (vac.getTotalNumber()*percentage)/100;
            row[2] = vacAllocationNo;
            model.addRow(row);
        
        }
       
        
        
    }//GEN-LAST:event_viewStateAllocationJButtonActionPerformed

