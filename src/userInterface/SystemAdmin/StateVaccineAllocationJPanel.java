    private void manuNameJComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manuNameJComboActionPerformed
        // TODO add your handling code here:
      
        ManufacturerEnterprise me = (ManufacturerEnterprise) manuNameJCombo.getSelectedItem();
       
        DefaultTableModel model = (DefaultTableModel) currInventoryJTable.getModel();
        ArrayList<Vaccine> vaccineList = new ArrayList<>();
        
        model.setRowCount(0);
        if(me!=null)
        {
              for(VaccineType type : system.getVaccineDirectory().getVaccineList())
            {
            
             for(Batch b : me.getBatchDirectory().getBatchList())
             {        

        
            if(b.getManufacturerInventoryStatus().equalsIgnoreCase(b.INSTOCK))
            {
           
              
                for(Vaccine v : b.getVaccineList())
                    {
                        if(v.getVaccineType().equals(type))
                        {
                        boolean flag = checkExisting(v, vaccineList);
                        if(flag == false)
                        {
                            Object[] row = new Object[5];
                            row[0] = v.getVaccineType().getVaccineTypeName();
                            row[1] = v;
                            row[2] = v.getTotalNumber();
                            row[3] = v.getvPrice();
                            row[4] = b.getManufacturerInventoryStatus();
                            model.addRow(row);
                            vaccineList.add(v);
                            break;
                           
                        }
                        }
                    
                    }

                
                }
             }
                
           }
         }
       

        

    }//GEN-LAST:event_manuNameJComboActionPerformed

      public boolean  checkExisting(Vaccine vName, ArrayList<Vaccine> vList)
    {
    
        if(!vList.isEmpty())
        {
            for(Vaccine v : vList)
            {
                if(v.getVaccineName().equals(vName.getVaccineName()) && v.getVaccineType().getVaccineTypeName().equals(vName.getVaccineType().getVaccineTypeName()))
                {
                
                    return true;
                
                }
            
            }
        
        }
        return false;
    }
    
  
