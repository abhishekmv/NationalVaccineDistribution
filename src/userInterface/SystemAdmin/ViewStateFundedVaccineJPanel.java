    private void btnViewStatesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewStatesActionPerformed
        // TODO add your handling code here:
        VaccineType vt = (VaccineType) comboType.getSelectedItem();

         if(comboType.getSelectedItem()==null)
        {
             JOptionPane.showMessageDialog(null, "No state Found!");
             return;
             
        
        }
        
        if(!vt.getStateFundedList().isEmpty()){
            DefaultTableModel dtm = (DefaultTableModel) fundingJTable.getModel();
            dtm.setRowCount(0);

            for(String s : vt.getStateFundedList()){
                Object[] row = new Object[1];
                row[0] = s;
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "This Vaccine is not funded in any state!");
        }
    }//GEN-LAST:event_btnViewStatesActionPerformed

