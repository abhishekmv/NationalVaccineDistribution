    private void goJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goJButtonActionPerformed
        // TODO add your handling code here:
        
       String region = (String) regionJComboBox.getSelectedItem();
       for(Enterprise e : enterpriseDirectory.getEnterpriseList())
       {
           if(e.getEnterpriseType().equals(Enterprise.EnterpriseType.Distributor))
           {
           
               DistributorEnterprise distributorEnterprise = (DistributorEnterprise) e;
               
               if(!distributorEnterprise.getWarehouseList().isEmpty())
               {
               for(Warehouse w : distributorEnterprise.getWarehouseList())
               {
                   if(w.getWarehouseRegion().equals(region))
                   {
                   
                       DefaultTableModel model = (DefaultTableModel) distributorJTable.getModel();
                       model.setRowCount(0);
                       for(Batch b : w.getBatchDirectory().getBatchList())
                       {
                             Object [] row = new Object[8];
                             for(Vaccine v : b.getVaccineList())
                             {
                             
                                 row[0] = v.getVaccineType().getVaccineTypeName();
                                 row[1] = v.getVaccineName();
                                 row[2] = v.getManufacturedDate();
                                 row[3] = v.getExpireDate();
                                 row[4] = v.getVaccineBatchNo();
                               
                                 row[7] = v.getVaccineType().getFundingInfo();
                                 break;
                             }
                             
                              row[5] = b.getTotalQuantity();
                                row[6] = b.getDistributorInventoryStatus();
                              model.addRow(row);
                             
                       }
                   }
                   
                   
               }
               }
               else
                   {
                       JOptionPane.showMessageDialog(null, "No Distributor warehouse region added yet!");
                   
                   }
               
           
           }

       }
    }//GEN-LAST:event_goJButtonActionPerformed

