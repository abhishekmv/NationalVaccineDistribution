   private void populateDistributorTable()
   {
   
       if(!enterpriseDirectory.getEnterpriseList().isEmpty())
       {
       
           for(Enterprise e: enterpriseDirectory.getEnterpriseList())
           {
              if(e.getEnterpriseType().equals(Enterprise.EnterpriseType.Distributor))
              {
              
                  txtName.setText(e.getName());
                  for(UserAccount ua : e.getUserAccountDirectory().getUserAccountList())
                  {
                  
                      if(ua.getRole().getRoleName().equalsIgnoreCase(Role.RoleType.DistributorAdminRole.getValue()))
                      {
                      
                      txtAdmin.setText(ua.getPerson().getFirstName()+" "+ua.getPerson().getLastName());
                      
                      }
                  
                  }
              
              }
              
           }
       
       }
   
   
   }
   
   private void populateRegionComboBox()
   {
   
       regionJComboBox.removeAllItems();
       
       for(Enterprise e : enterpriseDirectory.getEnterpriseList())
       {
           if(e.getEnterpriseType().equals(Enterprise.EnterpriseType.Distributor))
           {
           
               DistributorEnterprise distributorEnterprise = (DistributorEnterprise) e;
               
               for(Warehouse w : distributorEnterprise.getWarehouseList())
               {
                   regionJComboBox.addItem(w.getWarehouseRegion());
                   
               }
               
           
           }

       }
   
   }

